<div class="infoblock">
    <div class="label"><h2>What is an HSA?</h2></div>

    <div class="info">
        <p>
            A Health Savings Account (HSA) is like a personal savings accounts, but the money saved there is used to pay for you and your family’s qualified health care expenses.
        </p>
    </div><!--/.info-->
    <div class="clear"></div>
</div><!--/.infoblock-->

<div class="infoblock">
    <div class="label"><h2>Why consider an HSA?</h2></div>

    <div class="info">

            <ul>
                <li>You own and control the money in your health savings account.</li>
                <li>You do not pay taxes on money going into your HSA or any associated investment growth.</li>
                <li>Money contributed into the account is with pre-tax dollars, which reduces your taxes per your tax rate.</li>
                <li>Any unused money at the end of the year rolls over (stays in your account) year over year.</li>
                <li>It is portable as you move between employers.</li>
            </ul>

    </div><!--/.info-->
    <div class="clear"></div>
</div><!--/.infoblock-->

<div class="infoblock">
    <div class="label"><h2>Who can participate?</h2></div>

    <div class="info">
        <p>
            You must be under the age of 65 and participate in a High-Deductible Health Plan (HDHP). If your spouse uses your insurance as secondary coverage, s/he must also be enrolled in a HDHP. The HDHP must be your only health insurance; however, having dental, vision, disability and long term care insurance does not disqualify you from having an HSA.
        </p>
    </div><!--/.info-->
    <div class="clear"></div>
</div><!--/.infoblock-->

<div class="infoblock">
    <div class="label"><h2>How much may I contribute?</h2></div>

    <div class="info">
        <p>
            Each year the federal government determines the maximum dollar amount both you and your employer may contribute. If you are aged 55 years or older, you may also make “catch-up” contributions in addition to the maximum contribution guidelines.
        </p>
    </div><!--/.info-->
